# capitalize()	Converts the first character to upper case
ism = "anvar"
print(ism.capitalize())
familiya = "botirov"
print(familiya.capitalize())


# casefold()	Converts string into lower case
matn ="Hello, And Welcome To My World!"
print(matn.casefold())
matn2 = "This method is similar to the"
print(matn2.casefold())


# center()	Returns a centered string
mashina = "Lacetti"
print(mashina.center(15))
mashina2 = "Cobalt"
print(mashina2.center(20))


# count()	Returns the number of times a specified value occurs in a string
meva = "I love apples, apple are my favorite fruit"
print(meva.count("apple"))
meva2= "i love bananas, banana are my favorite fruit banana"
print(meva2.count("banana"))


